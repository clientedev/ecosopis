import { z } from 'zod';
import { insertUserSchema, insertProductSchema, insertOrderSchema, insertMessageSchema, products, users, orders, messages } from './schema';

export const errorSchemas = {
  validation: z.object({
    message: z.string(),
    field: z.string().optional(),
  }),
  notFound: z.object({
    message: z.string(),
  }),
  internal: z.object({
    message: z.string(),
  }),
};

